package hotel_svc

import (
	"net/http"
)

func AddHotel(w http.ResponseWriter, r *http.Request) {
	if r.Method != "POST" {
		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		return
	}
	w.Write([]byte("Adding hotel"))
}

